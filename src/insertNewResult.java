    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        String rollNo = jTextField1.getText();
        String database_concept = jTextField2.getText();
        String microprocessor = jTextField3.getText();
        String mathematics = jTextField4.getText();
        String Digital_Communication = jTextField5.getText();
        String software_engineering = jTextField6.getText();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm?autoReconnect=true&useSSL=false", "root", "3434!Natun?!%@$#");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollNo='" + rollNo + "'");
            if (rs.next()) {
                st.executeUpdate("insert into result (rollNo, database_concept,microprocessor, mathematics,Digital_Communication,software_engineering) values('" + rollNo + "','" + database_concept + "','" + microprocessor + "','" + mathematics + "','" + Digital_Communication + "','" + software_engineering + "')");
                JOptionPane.showMessageDialog(null, "Successfully Updated");
                setVisible(false);
                new insertNewResult().setVisible(true);
                st.close();
            } else {
                JOptionPane.showMessageDialog(null, "Roll number does not exist in our database");
                setVisible(false);
                new insertNewResult().setVisible(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Connection error");
        }
    }//GEN-LAST:event_jButton11ActionPerformed

